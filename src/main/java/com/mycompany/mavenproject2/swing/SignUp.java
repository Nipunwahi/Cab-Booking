                {
                    javax.swing.JOptionPane.showMessageDialog(null,"Username already exists") ;
                }
                else
                {
                    db.Insert_new_user(name_text, email_text, username_text, pass_text,0, contact_text);
                    javax.swing.JOptionPane.showMessageDialog(null,"SignUp Successful") ;
                }
