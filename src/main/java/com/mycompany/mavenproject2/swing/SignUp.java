    static boolean isValid(String email) {
        String regex = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        return email.matches(regex);
    }
    static boolean isValidNum(String contact)
    {
        if(contact.length()==10)
        {
            try
            {
                Long.parseLong(contact);
            }
            catch(Exception e)
            {
                return false;
            }
        }
        else
        {
            return false;
        }
        return true;  
    }
